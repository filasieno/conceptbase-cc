%  ====================
%  Exported predicates:
%  ====================
%  ***************************************************************************
%
%  NewIntegrityConstraint( _ranges, _rangeform, _IcId)
%
%  This predicate processes a new integrity condition. Provided
%  is that an object (with the identifier _IcId) has already been created,
%  which represents the integrity condition.
%  Procedure:
%        - Storing the entire integrity constraint.
% 	- Processing the formula: with each literal of the formula
% 	  (insofar as it touches the database):
% 	  - Identifying the classes whose instances may be represented by this literal
% 	    and
% 	  - Creating the so-called simplified instance of the integrity
% 	    condition with respect to this literal.
% 	  - Searching for and processing a rule that generates objects that may be
%            affected by this literal.
% 	  - Storing the found information.
%
%  _ranges    : specifications about the occurring variables
%  _rangeform : the formula of the new integrity constraint; for the exact
% 	       format see elsewhere, (i)
%  _IcId      : identifier of the object that represents the integrity
%               condition (i)
%
%  ***************************************************************************

'NewIntegrityConstraint'( _ranges, _rangeform, _IcId) :-
	'VarsOfRangeform'(_rangeform,_vars),
	convert(_rangeform,_rangeformO),  % kvt
	'RangeToEvaForm'(_vars,_ranges,_rangeformO, 'TRUE',_evaform, _),
	store_origConstraint(_evaform, _IcId),
	generateSpecializations(_vars,_ranges,_rangeformO,_IcId),
	!,
	'error_number@F2P'(0).  % Compilation must have been successful!
generateSpecializations(_vars,_ranges, _rangeform, _IcId) :-
  'PassThruRangeform'(_rangeform,
	  'ExamIcLiterals'(_ranges, _rangeform, _IcId, _vars)),
  !.
%  ***************************************************************************
%
%  NewRule(_ranges, _rangeform, _RuleId)
%
%  When a new rule is introduced, it is processed with this predicate
%  etc. It is assumed that it is already stored as an object
%  stored. Depending on the type of literals and the degree of
%  instantiation, the classes are determined whose instances may be affected by this
%  literal, as well as additional restriction tests (analogous to the
%  procedure for a new integrity constraint).
%  If the objects generated by this rule enter an integrity test,
%  then additional references to those affected by the condition literals are combined
%  these objects (or their classes) are created.
%
%  _rangeform : deductive rule in the form rangerule(_cond,_concl) (i)
%  _concl_lit : conclusion part of the rule; this is a single literal, (i)
%  _RuleId     : the identifier of the object representing the rule (i)
%
%  ***************************************************************************

'NewRule'(_ranges, rangerule(_rangecond,_rangeconcl), _RuleId) :-
	'VarsOfRangeform'(_rangecond,_vars),
	%  1. Storing the complete rule:
	%
	%  1a. For this it must be determined from which class the rule can derive instances:
	%

	'ConcernedClass'(_vars,_ranges,_rangeconcl,_class),
	'AdmissableConclusionClass'(_class),
	convert(_rangecond,_rangecondO),
	'RangeToEvaForm'(_vars,_ranges, rangerule(_rangecondO,_rangeconcl),'TRUE', evarule(_evaform_cond,_evaform_concl),_),
	%  1c. And now the ... are complete rule, in internal formats, and the affected class stored:
	%

	  store_origRule(_evaform_cond, _evaform_concl, _RuleId, _class, ruleinfo(_rangecondO,_rangeconcl,_ranges,_vars)),
	%  2. Compiling the rule:
	%
	%  2a. Compiling the rule w.r.t. insert operations:
	%
	%  Check whether from the class whose instances may be affected by the rule-head literal, references regarding integrity constraints or rules emanate that would have to be tested during an insert operation:
	%

	findall( _SimpIcIdIns,
		   (
			retrieve_BDMFormula( 'applyConstraintIfInsert@BDMCompile'( _icID, _class, _SimpIcIdIns, _, _)),
			retrieve_proposition('P'(_icID,_,_,_))  % 26-May-1995 LWEB
		   ),
		_ListOfSimpIcIdsInsert),
 	findall( _SimpRuleIdIns,
		retrieve_BDMFormula('applyRuleIfInsert@BDMCompile'( _RuleId, _class, _SimpRuleIdIns, _, _, _, _)),
		_ListOfSimpRuleIdsInsert),
	%  And compile the found formulas:
	%

	((_ListOfSimpIcIdsInsert = [],
	  _ListOfSimpRuleIdsInsert = []
	 );
	 'PassThruRangeform'( _rangecondO, 'ExamCondLitsForRuleOrIc'('Insert', _ranges, _rangecondO, _rangeconcl, _RuleId, _ListOfSimpIcIdsInsert, _ListOfSimpRuleIdsInsert, _vars))),
	%  2b. With respect to delete operations:
	%
	%  Check whether references concerning integrity constraints or rules that would need to be tested during a delete operation emanate from the class whose instances could be affected by the rule-head literal:
	%

	findall( _SimpIcIdDel,
		(
			retrieve_BDMFormula('applyConstraintIfDelete@BDMCompile'( _IcId, _class, _SimpIcIdDel, _, _)),
			retrieve_proposition('P'(_IcId, _, _, _))  % 26-May-1995 LWEB
		),
		_ListOfSimpIcIdsDelete),
	findall( _SimpRuleIdDel,
		retrieve_BDMFormula('applyRuleIfDelete@BDMCompile'( _RuleId, _class, _SimpRuleIdDel, _, _, _, _)),
		_ListOfSimpRuleIdsDelete),
	%  And compile the found formulas:
	%

 	((_ListOfSimpIcIdsDelete = [],
	  _ListOfSimpRuleIdsDelete = []
	 );
	 'PassThruRangeform'( _rangecondO, 'ExamCondLitsForRuleOrIc'( 'Delete', _ranges, _rangecondO, _rangeconcl, _RuleId, _ListOfSimpIcIdsDelete, _ListOfSimpRuleIdsDelete, _vars))),
	!,
	%  Compilation must have been successful!
	%

	'error_number@F2P'(0).
%  16-Jul-1991/MJf: error handling
%

'NewRule'(_ranges, rangerule(_rangecond,_rangeconcl), _RuleId) :-
	increment('error_number@F2P'),
	report_error('NORULE', 'BDMCompile', [objectName(_RuleId)]),
	!,
	fail.
