   fail.
%  **************************************************************************
%
%  SimplifyRangeform( _ranges, _rangeform, _InsDel,_lit, _vars,
%                     _simplifiedrangeform)
%
%  _ranges      : table of occurring variables with their "ranges" (i)
%  _rangeform   : the integrity constraint (i) to be simplified,
%  _InsDel      : indicator whether the simplified IC is to be tested during
%                 an insert or delete operation (i).
%  _lit         : with respect to which the IC is to be simplified (i),
%  _vars        : variables of the formula, cf. ExamIcLiterals (i),
%  _simplifiedrangeform : the simplified integrity constraint (o),
%
%  Here the specified integrity constraint is simplified with respect to one
%  of its literals.
%
%  **************************************************************************

'SimplifyRangeform'(_ranges, _rangeform, _InsDel, _lit, _vars,
                  _simplifiedrangeform) :-
	_lit =.. [ _LiteralName | _LiteralArgs],
		%  1. Eliminate variables that appear as arguments of the
		%     literal and are universally quantified but do not lie in the
		%     scope of an existential quantifier:

	'ElimQuantsInRangeform'(_ranges,_LiteralArgs,_vars,_rangeform,
		              _rangeform_woquants,_all_elimed),
		%  2. Under certain circumstances omit the literal from
		%     the formula and simplify it:

        'InsertOrDelete'(_all_elimed, _InsDel, _lit, _rangeform_woquants,
                       _simplifiedrangeform),
'WriteTrace'(veryhigh,'BDMCompile',
           [idterm('SimplifyRangeform'(_InsDel,_lit)),'--->',idterm(_simplifiedrangeform)]),
	!.
%  **************************************************************************
%
%  ElimQuantsInRangeform( _ranges,_LiteralArgs,_vars,_rangeform,
%                         _rangeform_woquants, _all_elimed)
%
%  Delete quantifications of those variables that occur in the arguments
%  of a literal, are universally quantified, but do not lie in the scope of
%  an existential quantifier.
%
%  _ranges      : table of occurring variables with their "ranges" (i)
%  _LiteralArgs       : the arguments of a literal (i),
%  _vars         : cf. ExamIcLiterals (i),
%  _rangeform         : formula to search, (i)
%  _rangeform_woquants: the same formula but without the quantification of the
%                       variables that are arguments of the literal and
%                       are also listed in vars(_,_b), (o)
%  _all_elimed        : indicates whether all quantifications of variables
%                       occurring in the arguments of the literal could be
%                       deleted (yes) or not (no). (o)
%
%  **************************************************************************
%  Done, nothing more to do.

'ElimQuantsInRangeform'(_ranges, [], _, _rangeform, _rangeform, yes) :- !.
%  The first (current) argument is a variable that is also listed in
%  _AllquVars (i.e. the variable is universally quantified in the formula
%  and no existential quantifier appears before it). Therefore the
%  quantification of the variable is removed from the formula.

'ElimQuantsInRangeform'(_ranges, [ _arg | _args_rest], vars(_a,_b),
                      _rangeform, _rangeform_woquants, _all_elimed) :-
	member( _arg, _b),
	!,
	'ElimQuant'( _arg, _rangeform, _rangeform_woquant),
	'ElimQuantsInRangeform'(_ranges,_args_rest, vars(_a,_b),
                              _rangeform_woquant,
	                      _rangeform_woquants, _all_elimed).
%  The current argument is indeed a variable, but it does not appear
%  in vars(_,_b), i.e. nothing to do here, continue.
%  In particular, _all_elimed = no!

'ElimQuantsInRangeform'(_ranges, [ _arg | _args_rest], vars(_a,_b),
                      _rangeform, _rangeform_woquants, no) :-
	member(_arg,_a),
	!,
	'ElimQuantsInRangeform'(_ranges, _args_rest, vars(_a,_b), _rangeform,
                              _rangeform_woquants, _).
%  The first (current) argument is a constant;
%  nothing to do here, continue.

'ElimQuantsInRangeform'(_ranges, [_arg|_args_rest], _vars,
                      _rangeform, _rangeform_woquants, _all_elimed) :-
	!,
	'ElimQuantsInRangeform'(_ranges, _args_rest, _vars, _rangeform,
                              _rangeform_woquants, _all_elimed).
%  **************************************************************************
%
%  InsertOrDelete( _all_elimed, _InsDel, _lit, _rangeformWoQuants,
%                             _rangeformWoLit)
%
%  Here is finally the so-called simplified instance of an IC
%  created with respect to one of its literals. The quantifications
%  of variables occurring in this literal were, where permitted,
%  already deleted previously.
%
%  _all_elimed        : indicates whether the quantifications of all variables
%                       occurring in the literal were deleted (yes) or not (no), (i)
%  _InsDel            : indicates whether _rangeformWoQuants is to be specialized
%                       with respect to insertion ('Insert') or deletion ('Delete')
%                       of _lit, (i)
%  _lit               : the literal from the formula with respect to which the so-called
%                       simplified instance of the formula is built, (i)
%  _rangeformWoQuants : the formula in which the required deletions of
%                       quantifications were performed, (i)
%  _rangeformWoLit    : if _all_elimed=yes and _InsDel=Insert, then
%                       negated occurrences of _lit in _rangeformWoQuants
%                       are replaced by FALSE or eliminated (o)
%
%  **************************************************************************
%  If not all variables of the inspected literal _lit were
%  eliminated, then _rangeform cannot be simplified further:

'InsertOrDelete'(no, 'Delete', _lit, _rangeform, _rangeform) :- !.
'InsertOrDelete'(no, 'Insert', _lit, _rangeform, _rangeform) :- !.
%  For 'Delete' no literal can disappear:

'InsertOrDelete'(yes, 'Delete', _lit, _rangeform, _rangeform) :- !.
%  negative _lit occurs in the (implicitly negative) literals
%  behind a 'forall': replace it by FALSE, i.e.
%  simply delete it from the list
%  ### Assumption: _lit occurs at most once in _lits; this
%  is reasonable since x and x = x or x = x

'InsertOrDelete'(yes, 'Insert', _lit,
               forall(_vars,_lits,_rangeF),
               forall(_vars,_newlits,_rangeF)) :-
   member(_lit,_lits),
   delete(_lit,_lits,_newlits),
   !.
%  search in an inner 'forall':

'InsertOrDelete'(yes, 'Insert', _lit,
               forall(_vars,_lits,_rangeF),
               forall(_vars,_lits,_rangeFwoLit)) :-
  !,
  'InsertOrDelete'(yes, 'Insert',_lit,_rangeF,_rangeFwoLit).
%  Search in conjunctions and disjunctions:

'InsertOrDelete'(yes, 'Insert', _lit,
               and(_rF1,_rF2),
               and(_newrF1,_newrF2)) :-
   'InsertOrDelete'(yes, 'Insert', _lit, _rF1, _newrF1),
   'InsertOrDelete'(yes, 'Insert', _lit, _rF2, _newrF2),
   !.
'InsertOrDelete'(yes, 'Insert', _lit,
               or(_rF1,_rF2),
               or(_newrF1,_newrF2)) :-
   'InsertOrDelete'(yes, 'Insert', _lit, _rF1, _newrF1),
   'InsertOrDelete'(yes, 'Insert', _lit, _rF2, _newrF2),
   !.
%  individually occurring negative literal
%  it is replaced by FALSE on 'Insert'

'InsertOrDelete'(yes, 'Insert', _lit,
               not(_lit),
               'FALSE') :- !.
%  any positive literal (including TRUE, FALSE)
%  it cannot be replaced on 'Insert'

'InsertOrDelete'(yes, 'Insert', _lit, _somelit, _somelit) :- !.
%  ============================================================================
%  ***************** Predicates that process rules: **********************
%  ============================================================================
%  ***************************************************************************
%
%  ExamCondLitsForRuleOrIc( _sign,_Literal, _InsDelConcl ,
%                           _RuleCondFormula, _RuleConcl, _RuleId,
%                       _ListOfSimpIcIds, _ListOfSimpRuleIds, _InsDel,_vars)
%
%  Here each individual literal of a rule's condition formula is pro-
%  cessed: creating the simplified form of the rule, creating references to other
%  integrity constraints or rules, ...
%  The affected class and the simplified form of the rule are determined
%  and stored; finally this simplified form is used to create the
%  references to other simplified rules or integrity constraints
%  created that concern instances of the class affected by the conclusion literal of the
%  current rule. Finally it must be examined whether
%  whether instances of the class affected by the current literal of the current formula
%  affected class itself can be derived by a rule,
%  such a rule must then be processed using the same procedure.
%
%  _sign : sign of the Literals (i).
%  _lit               : The current literal of the condition formula of the current
%                       rule that is now to be processed, (i)
%  _RuleCondFormula   : the condition formula of the current rule, (i)
%  _RuleConcl         : the conclusion literal of the current rule, (i)
%  _RuleId            : the identifier of the object that represents the current
%                       rule, (i)
%  _ListOfSimpIcIds   : a list of identifiers of simplified forms
%                       of integrity constraints into which instances of the clas-
%                       se are involved in that are affected by the conclusion literal of the current-
%                       all rules are affected, (i)
%  _ListOfSimpRuleIds : same with simplified rules, (i)
%
%  ***************************************************************************
%  similar to ExamIcLiterals
%  Weak litarals are for example comparison literals

'ExamCondLitsForRuleOrIc'( _sign,_lit,_InsDelConcl, _ranges,
                         _rangecond, _rangeconcl, _RuleId,
                         _ListOfSimpIcIds, _ListOfSimpRuleIds,_vars) :-
  'WeakLiteral'(_lit),
  !.
'ExamCondLitsForRuleOrIc'( _sign,_lit,_InsDelConcl, _ranges,
                         _rangecond, _rangeconcl, _RuleId,
                         _ListOfSimpIcIds, _ListOfSimpRuleIds,_vars) :-
