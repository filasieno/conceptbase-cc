%
%  ExamIcLiterals(_sign,_lit,_ranges,_rangeform,_IcId,_vars)
%
%  Here the integrity constraint formula with respect to a certain
%  of its literals is processed, i.e.
% 	1. Recognizing the affected class
% 	2. Creating the simplified instance of the integrity constraint
% 	3. Check whether rules must be observed
% 	4. Storing the found information.
%
%  _sign: sign of the literal (i)
%  _lit       : The literal with respect to which the integrity constraint is processed, (i)
%  _ranges    : table mapping variables to their "ranges" (classes) (i)
%  _rangeform : the formula of the integrity constraint that is processed w.r.t. the literal
%               is to be processed, (i)
%  _IcId      : the identifier of the object that represents the integrity constraint
%               represents, (i)
%  _vars      : information about the variables of the formula: vars(_a,_b)  (i)
%                _a = all variables
%                _b = all universally quantified variables that do not lie in the
%                     scope of an existential quantifier.
%
%  **************************************************************************
%  If _lit is a 'weak literal' (comparison operators, instances of
%  functions such as COUNT), then no 'simplified rangeforms' are
%  generated, because changes to the extension of this literal cannot
%  (so easily) be determined incrementally.

'ExamIcLiterals'(_sign, _lit, _ranges, _rangeform, _IcId, _vars) :-
  'WeakLiteral'(_lit),
  !.
'ExamIcLiterals'(_sign, _lit, _ranges, _rangeform, _IcId, _vars) :-
  'ConcernedClass'(_vars, _ranges, _lit, _class),
  'ExamIcLiteralsCC'(_sign, _lit, _class, _ranges, _rangeform, _IcId, _vars),
  !.

'ExamIcLiteralsCC'(_sign, _lit, _class, _ranges, _rangeform, _IcId, _vars) :-
  noTriggerLiteral(_class,_lit),
  !.
% 3-Jun-2004/M.Jeusfeld: check for forbidden orccurences of query classes
